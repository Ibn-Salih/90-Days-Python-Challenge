# Create a simple "countdown" program that counts down from 10 to 1
#  using a while loop.

# This program counts down from 10 to 1 using a while loop
count_down = 10

# Loop until count_down is less than 1
while count_down >= 1:
    print(count_down) # Print the current count
    count_down -= 1 # Decrease the count by 1

